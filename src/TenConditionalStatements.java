public class TenConditionalStatements {
    public static void main(String[] args) {
        int a= 10;
        if(a==10){
            System.out.println("Hello World!");
        }
        //while loop
        int count =1;
        while(count != 5) {
            System.out.println(count);
            count++;
        }
        //for loop
        for(int countt=1; countt !=5; countt++){
            System.out.println(countt);
        }
/* When you know how many times the loop is going to run, use the for loop, when you don't know how many times
    loop is going to run , use the while loop*/
    }
}
